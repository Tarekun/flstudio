def structure_dataset(data_path: str, users_number: int):
    """Function to structure the FEMNIST dataset by user so it can be used for a
    federated learning simulation.

    Parameters
    ----------
    data_path: str
        Path to the directory containing the .json files generated by the LEAF preprocess.sh script.
        For more info see: https://github.com/TalwalkarLab/leaf/blob/master/data/femnist/README.md
    users_number: int
        The number of users to extract
    """
    included = 0
    dataloaders_by_user = []
    files = [f for f in os.listdir(data_path) if f.endswith(".json")]
    files.sort()

    for file_name in files:
        with open(os.path.join(data_path, file_name), "r") as file:
            print(f"Parsing file {file_name}")
            # this json object has 3 keys:
            # `users` list of user ids,
            # `num_samples` list of images per user,
            # `user_data` (flattened) image data and label, partitioned per user
            dataset = json.load(file)
            user_data = dataset["user_data"]
            for user_id in user_data:
                x_list, y_list = user_data[user_id]["x"], user_data[user_id]["y"]
                print(
                    f"\tIncluding user: {user_id} having {len(x_list)} images with id: {included}"
                )
                user_loader = DataLoader(
                    FemnistClientDataset(x_list, y_list, transform),
                    batch_size=batch_size,
                    shuffle=True,
                )
                dataloaders_by_user.append(user_loader)

                included += 1
                if included >= users_number:
                    return dataloaders_by_user

    return dataloaders_by_user


