# Define transformations for the training and testing sets
transform = transforms.Compose(
    [
        transforms.ToTensor(),
        transforms.Normalize((0.5,), (0.5,)),  # Normalize the data to [-1, 1]
    ]
)


def split_dataset(dataset, ratio=0.9):
    train_size = int(ratio * len(dataset))
    val_size = len(dataset) - train_size
    train_subset, val_subset = random_split(dataset, [train_size, val_size])
    return train_subset, val_subset


class FemnistClientDataset(Dataset):
    def __init__(self, inputs: list[list[float]], labels: list[int], transform=None):
        if len(inputs) != len(labels):
            raise Exception(
                f"Different number of inputs ({len(inputs)}) and labels ({len(labels)})"
            )
        self.x, self.y = inputs, labels
        self.transform = transform

    def __getitem__(self, index):
        target_data = self.y[index]
        input_data = np.array(self.x[index]).reshape(28, 28)
        if self.transform:
            input_data = self.transform(input_data)

        return input_data.to(torch.float32), target_data

    def __len__(self):
        return len(self.y)


